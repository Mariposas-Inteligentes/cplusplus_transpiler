print("Lists_______________")
print("Ints")
x = [1, 2]
z = sum(x)
print(sum(x, z))

print("Floats")
x = [1.0, 2.0]
y = 3.0
z = sum(x, y)
print(sum(x, z))

print("Tuples_______________")
print("Ints")
x = (1, 2)
z = sum(x)
print(sum(x, z))

print("Floats")
x = (1.0, 2.0)
y = 3.0
z = sum(x, y)
print(sum(x, z))

print("Sets_______________")
x = {0, 1, 2, 3}
y = 1
print(sum(x, z))

print("Dicts_______________")
x = {0: "0", 1: "1", 2: "2", 3: "3"}
y = 1
print(sum(x, z))
